import pandas as pd
import numpy as np
import streamlit as st

# ---------- КРИТЕРИИ ----------

def compute_efficacy(df, group_means_df, group_col, deficit_groups, normal_groups, effect_markers):
    deficit_mask = df[group_col].isin(deficit_groups)
    normal_mask = df[group_col].isin(normal_groups)

    E_list = []

    for marker in effect_markers:
        if marker not in group_means_df.columns:
            continue

        E_plus = df.loc[deficit_mask, marker].mean()
        E_minus = df.loc[normal_mask, marker].mean()

        if pd.isna(E_plus) or pd.isna(E_minus) or np.isclose(E_plus, E_minus):
            continue

        E_d = group_means_df[marker]
        R_d = (E_d - E_plus) / (E_minus - E_plus)

        Eff = pd.Series(index=group_means_df.index, dtype=float)
        for d in group_means_df.index:
            R = R_d.loc[d]
            if R <= 0:
                Eff.loc[d] = 0.0
            elif 0 < R <= 1:
                Eff.loc[d] = R
            elif 1 < R < 2:
                Eff.loc[d] = 2 - R
            else:
                Eff.loc[d] = 0.0

        if Eff.max() > 0:
            Eff = Eff / Eff.max()
        E_list.append(Eff)

    if not E_list:
        return pd.Series(0.0, index=group_means_df.index)

    E_mean = pd.concat(E_list, axis=1).mean(axis=1)
    return E_mean


def compute_toxicity(group_means_df, toxic_markers):
    used = [m for m in toxic_markers if m in group_means_df.columns]
    if not used:
        return pd.Series(0.0, index=group_means_df.index)

    if 0.0 not in group_means_df.index:
        # нет дозы 0 как контроля – вернём нули
        return pd.Series(0.0, index=group_means_df.index)

    control = group_means_df.loc[0.0]

    Toxic_raw = pd.Series(0.0, index=group_means_df.index, dtype=float)

    for m in used:
        T_minus = control[m]
        for d in group_means_df.index:
            T_d = group_means_df.loc[d, m]
            if T_minus == 0 or pd.isna(T_minus) or pd.isna(T_d):
                delta = 0.0
            else:
                delta = (T_d - T_minus) / T_minus
                if delta < 0:
                    delta = 0.0
            Toxic_raw.loc[d] += delta

    Toxic_raw = Toxic_raw / len(used)

    max_val = Toxic_raw.max()
    if max_val > 0:
        return Toxic_raw / max_val
    else:
        return pd.Series(0.0, index=group_means_df.index)


def compute_balance(group_means_df, balance_markers):
    used = [m for m in balance_markers if m in group_means_df.columns]
    if not used:
        return pd.Series(1.0, index=group_means_df.index)

    if 0.0 not in group_means_df.index:
        return pd.Series(1.0, index=group_means_df.index)

    control = group_means_df.loc[0.0]

    D = pd.Series(0.0, index=group_means_df.index, dtype=float)
    for d in group_means_df.index:
        vals = []
        for m in used:
            Xd = group_means_df.loc[d, m]
            Xminus = control[m]
            if Xminus == 0 or pd.isna(Xminus) or pd.isna(Xd):
                continue
            vals.append(abs((Xd - Xminus) / Xminus))
        D.loc[d] = np.mean(vals) if vals else 0.0

    max_D = D.max()
    if max_D > 0:
        return 1 - D / max_D
    else:
        return pd.Series(1.0, index=group_means_df.index)


def compute_score(efficacy, balance, toxicity, w_e, w_b, w_t):
    s = w_e + w_b + w_t
    if s == 0:
        w_e, w_b, w_t = 1.0, 0.0, 0.0
    else:
        w_e, w_b, w_t = w_e / s, w_b / s, w_t / s
    return w_e * efficacy + w_b * balance - w_t * toxicity


