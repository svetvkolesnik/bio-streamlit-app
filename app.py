st.title("Анализ дозозависимых эффектов и расчёт Score")

st.write("Загрузите CSV с данными (например, Rats_lastclear4.csv).")

uploaded = st.file_uploader("Файл CSV", type=["csv"])

if uploaded is not None:
    df = pd.read_csv(uploaded, sep=';', decimal=',')
    st.write("Первые строки данных:")
    st.dataframe(df.head())

    # выбор столбца с группами
    group_col = st.selectbox(
        "Столбец с названиями групп",
        options=df.columns.tolist(),
        index=df.columns.tolist().index("Группа") if "Группа" in df.columns else 0
    )

    # уникальные группы
    groups = df[group_col].unique().tolist()
    st.write("Найденные группы:", groups)

    # выбор групп дефицита и нормы
    deficit_groups = st.multiselect(
        "Группы дефицита/болезни",
        options=groups,
        default=[g for g in groups if "дефицит" in str(g).lower()]
    )
    normal_groups = st.multiselect(
        "Группы нормы",
        options=groups,
        default=[g for g in groups if "Отрицательный контроль" in str(g)]
    )

    # выбор столбца/столбцов с дозой
    st.write("Если в данных уже есть числовой столбец с дозой, выберите его.")
    dose_col_option = st.selectbox(
        "Столбец с дозой (или 'Создать из групп')",
        options=["Создать из групп"] + list(df.columns)
    )

    # если дозы создаём из групп – нужен маппинг
    if dose_col_option == "Создать из групп":
        st.write("Создаём дозу по группам. Укажите соответствие (группа → доза).")
        dose_map = {}
        for g in groups:
            val = st.text_input(f"Доза для группы '{g}' (пусто = исключить)", "")
            if val.strip() == "":
                dose_map[g] = np.nan
            else:
                try:
                    dose_map[g] = float(val.replace(",", "."))
                except ValueError:
                    dose_map[g] = np.nan
        df["dose"] = df[group_col].map(dose_map)
    else:
        df["dose"] = pd.to_numeric(df[dose_col_option], errors="coerce")

    df = df[~df["dose"].isna()].copy()

    st.write("Распределение доз:")
    st.write(df["dose"].value_counts().sort_index())

    # выбор маркеров
    numeric_cols = df.select_dtypes(include=[np.number]).columns.tolist()
    # удалим служебные столбцы
    if "dose" in numeric_cols:
        numeric_cols.remove("dose")

    effect_markers = st.multiselect(
        "Маркеры эффективности",
        options=numeric_cols,
        default=[c for c in numeric_cols if "Zn_blood" in c] or []
    )

    toxic_markers = st.multiselect(
        "Маркеры токсичности",
        options=numeric_cols,
        default=[c for c in numeric_cols if c in ["As_liver", "Pb_kidneys", "Cu_liver"]]
    )

    balance_markers = st.multiselect(
        "Маркеры баланса (обычно Zn по органам)",
        options=numeric_cols,
        default=[c for c in numeric_cols if c.startswith("Zn_")]
    )

    # веса
    st.write("Веса критериев (сумма будет автоматически нормирована):")
    w_e = st.number_input("w_e (эффективность)", min_value=0.0, max_value=1.0, value=0.6, step=0.05)
    w_b = st.number_input("w_b (баланс)", min_value=0.0, max_value=1.0, value=0.2, step=0.05)
    w_t = st.number_input("w_t (токсичность)", min_value=0.0, max_value=1.0, value=0.2, step=0.05)

    if st.button("Рассчитать Score"):
        # групповые средние
        cols_for_means = list(set(effect_markers + toxic_markers + balance_markers))
        gmeans = df.groupby("dose")[cols_for_means].mean()

        eff = compute_efficacy(
            df, gmeans,
            group_col=group_col,
            deficit_groups=deficit_groups,
            normal_groups=normal_groups,
            effect_markers=effect_markers
        )
        tox = compute_toxicity(gmeans, toxic_markers)
        bal = compute_balance(gmeans, balance_markers)

        score = compute_score(eff, bal, tox, w_e, w_b, w_t)

        res = pd.DataFrame({
            "dose": gmeans.index,
            "Efficacy": eff.values,
            "Balance": bal.values,
            "Toxicity": tox.values,
            "Score": score.values
        }).set_index("dose")

        st.subheader("Результат по дозам")
        st.dataframe(res.sort_values("Score", ascending=False))

        # скачать
        csv_bytes = res.to_csv().encode("utf-8")
        st.download_button(
            label="Скачать результаты в CSV",
            data=csv_bytes,
            file_name="dose_scores.csv",
            mime="text/csv"
        )